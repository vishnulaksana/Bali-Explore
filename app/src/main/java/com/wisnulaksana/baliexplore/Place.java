package com.wisnulaksana.baliexplore;

public class Place {

    private int id;
    private String namaTempat;
    private String harga;
    private String tipewisata;
    private String fasilitas;

    public int getId() {return id;}

    public void setId(int id) {this.id =id; }

    public String getNamaTempat() { return namaTempat;}

    public void setNamaTempat(String namaTempat) { this.namaTempat = namaTempat;}

    public String getHarga() { return harga;}

    public void setHarga(String harga) { this.harga = harga;}

    public String getTipewisata() { return tipewisata;}

    public void setTipewisata(String tipewisata) { this.tipewisata = tipewisata;}

    public String getFasilitas() { return fasilitas;}

    public void setFasilitas(String fasilitas) { this.fasilitas = fasilitas;}
}
